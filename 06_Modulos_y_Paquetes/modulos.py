#---------- Módulo de Operaciones ----------
#(Suma):
def suma(a, b):
    return a + b

#(Resta):
def resta(a, b):
    return a - b

#(Multiplica):
def multiplicacion(a, b):
    return a * b

#(Divide):
def division(a, b):
    if b == 0:
        return "Error: División por cero"
    return a / b

